class Dato {
    constructor(_descripcion, _value) {
        this._descripcion = _descripcion;
        this._value = _value;
    }

    getDescripcion() {
        return this._descripcion;
    }

    setDescripcion(descripcion) {
        this._descripcion = descripcion;
    }

    getvalue() {
        return this._value;
    }

    setvalue(value) {
        this._value = value;
    }

}